            out.push_str(TECH_ESSAY_OPENINGS.choose(rng).unwrap());
            out.push('\n');
            let n_det = rng.gen_range(2..=4);
            for _ in 0..n_det {
                out.push_str(TECH_DETAILS.choose(rng).unwrap());
                out.push(' ');
            }
            out.push('\n');
            out.push_str(ESSAY_CONCLUSIONS.choose(rng).unwrap());
